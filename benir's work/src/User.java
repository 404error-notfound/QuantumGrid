


public class User {
    String name;
    String email;
    String Password;
    public User(String name, String email, String password) {
        this.name = name;
        this.email = email;
        this.Password = password;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return Password;
    }


}
